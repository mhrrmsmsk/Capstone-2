const LangCtx = createContext()

export function LangProvider({ children }) {
  const [lang, setLang]   = useState('en')
  const [theme, setTheme] = useState('dark')

  useEffect(() => {
    document.documentElement.setAttribute('data-theme', theme)
  }, [theme])

  const toggleTheme = () => setTheme(t => t === 'dark' ? 'light' : 'dark')

  const t  = (key)              => T[lang][key] ?? T['en'][key] ?? key
  const tc = (name)             => lang === 'tr' ? (CROP_TR[name]    ?? name)             : name
  const tf = (key, fallback)    => lang === 'tr' ? (FEAT_TR[key]     ?? fallback ?? key)  : (fallback ?? key)
  const tv = (val)              => lang === 'tr' ? (CAT_VAL_TR[val]  ?? val)              : val
  return (
    <LangCtx.Provider value={{ lang, setLang, theme, toggleTheme, t, tc, tf, tv }}>
      {children}
    </LangCtx.Provider>
  )
}

export function useLang() { return useContext(LangCtx) }
